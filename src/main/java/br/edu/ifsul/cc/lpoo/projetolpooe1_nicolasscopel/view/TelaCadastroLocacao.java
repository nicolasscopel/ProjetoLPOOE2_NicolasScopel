    private void listarLocacoes() {

        PersistenciaJPA jpa = new PersistenciaJPA();
        jpa.conexaoAberta();

        List<Locacao> locacoes = jpa.getLocacoes();
        mascaraItemLista.clear();

        for (Locacao loc : locacoes) {
            mascaraItemLista.addElement(loc);

        }
        lstLocacoes.setModel(mascaraItemLista);
        jpa.fecharConexao();
    }

