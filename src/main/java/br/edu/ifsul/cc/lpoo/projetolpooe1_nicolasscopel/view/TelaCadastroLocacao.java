    private void txtBuscaLocacaoNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscaLocacaoNomeKeyReleased
       // TODO add your handling code here:
        String textoBusca = txtBuscaLocacaoNome.getText().trim();
        
        if(textoBusca.isEmpty()){
            //return;
        }
                
        PersistenciaJPA jpa = new PersistenciaJPA();
        jpa.conexaoAberta();

        List<Locacao> locacoes = jpa.getLocacoes(textoBusca);
        mascaraItemLista.clear();

        for (Locacao loc : locacoes) {
            mascaraItemLista.addElement(loc);

        }
        lstLocacoes.setModel(mascaraItemLista);
        jpa.fecharConexao();
    }//GEN-LAST:event_txtBuscaLocacaoNomeKeyReleased

    private void btnEditarLocacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarLocacaoActionPerformed
       
        Locacao locacaoSelecionada = lstLocacoes.getSelectedValue();
        
        if(locacaoSelecionada != null){
            TelaEditarLocacao editar = new TelaEditarLocacao(this, true);
            editar.setLocacao(locacaoSelecionada);
            editar.setVisible(true);
            listarLocacoes();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Nenhuma Locacao Selecionada");
            
        }
        
    }//GEN-LAST:event_btnEditarLocacaoActionPerformed

    private void btnExcluirLocacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirLocacaoActionPerformed
        // TODO add your handling code here:
        Locacao locacaoSelecionada = lstLocacoes.getSelectedValue();
        PersistenciaJPA jpa = new PersistenciaJPA();
        
        if(locacaoSelecionada != null){
            int confirmPanel = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja remover o veiculo " + locacaoSelecionada + " ?");

        if (confirmPanel == JOptionPane.YES_OPTION) {

            try {

                jpa.conexaoAberta();
                jpa.remover(locacaoSelecionada);
                jpa.fecharConexao();
                listarLocacoes();
            } catch (Exception ex) {
                System.out.println("Erro ao remover locacao selecionado!");

            } finally {
                jpa.fecharConexao();
            }
        }
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Nenhuma locacao selecionado! ");
        }
    }//GEN-LAST:event_btnExcluirLocacaoActionPerformed

