    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        jpa =  new PersistenciaJPA();
        

        Locacao nova = new Locacao();
        
        
        String dataEntrada = txtDataEntradaLocacao.getText();
        String dataSaida = txtDataSaidaLocacao.getText();
        Calendar dataEntradaCal = Calendar.getInstance();
        Calendar dataSaidaCal = Calendar.getInstance();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy"); // Formato da data (ajuste conforme necessário)

        
        nova.setNumeroVaga(txtVagaLocacao.getText());
        
        try {
           dataEntradaCal.setTime(formato.parse(dataEntrada));
           dataSaidaCal.setTime(formato.parse(dataSaida));
        } catch (Exception ex) {
        }
        
        if(dataSaidaCal != null){
            nova.setDataEntrada(dataEntradaCal);
        }
        if(dataSaidaCal != null){
            nova.setDataSaida(dataSaidaCal);
        }
        
        nova.setVeiculo((Veiculo)comboVeiculoLocacao.getSelectedItem());
        
       

         try{
             
            jpa.conexaoAberta();
            
            
            //PERSISTINDO LOCAÇÃO
            jpa.persist(nova);
            
            
            System.out.println("Locacao do veiculo " + nova.getVeiculo().getModelo() + " adicionado!");
            dispose();
        }catch(Exception ex){
            System.out.println("Erro ao adicionar locacao");
            dispose();
        }
      
        
        jpa.fecharConexao();
    }//GEN-LAST:event_btnSalvarActionPerformed

