    public void listarVeiculos(){
        comboVeiculoLocacao.removeAllItems();
        jpa = new PersistenciaJPA();
        jpa.conexaoAberta();
        List<Veiculo> lista = jpa.getVeiculos();
        for(Veiculo o: lista){
            comboVeiculoLocacao.addItem(o);
        }
        jpa.fecharConexao();
        
    } 

